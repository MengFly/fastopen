package com.pengfei.fastopen.utils;

/**
 * 字符串的工具类
 * Created by mengfei on 2017/7/22.
 */
public class TextUtils {

    public static CharSequence getText(CharSequence charSequence) {
        if (charSequence == null) {
            return "";
        } else {
            return charSequence;
        }
    }
}
